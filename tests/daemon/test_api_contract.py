# ── Test: undocumented routes (informational) ──────────────────────────────────

class TestUndocumentedRoutes:
    """Report routes on the daemon that are not in any spec.

    This test does NOT fail on undocumented routes — it only reports them
    as warnings so developers are aware of undocumented surface area.
    """

    def test_report_undocumented_routes(self, app_routes, capsys):
        participant_spec = _load_spec(PARTICIPANT_SPEC_PATH)
        host_spec = _load_spec(HOST_SPEC_PATH)

        # Build all spec paths (with full prefixes)
        spec_full_paths: set[tuple[str, str]] = set()

        for method, path in _spec_paths_to_route_set(participant_spec):
            spec_full_paths.add((method, "/api/participant" + path))

        for method, path in _spec_paths_to_route_set(host_spec):
            spec_full_paths.add((method, "/api/{session_id}" + path))

        # Global paths from x-global-paths section (not standard OpenAPI, handled manually)
        spec_full_paths.add(("POST", "/api/transcription-language"))
        spec_full_paths.add(("GET",  "/api/transcription-language/request"))

        undocumented = []
        for method, path in sorted(app_routes):
            # Skip FastAPI internal + catch-all proxy + static
            if path in _FASTAPI_INTERNAL_PREFIXES:
                continue
            # Catch-all proxy: /api/{path:path} rendered as /api/{path} in openapi
            if re.search(r"\{path[^}]*\}", path):
                continue
            if any(path.startswith(p.rstrip("/")) for p in _KNOWN_UNSPECCED_PREFIXES):
                continue
            # Check if this app path matches any spec path
            matched = any(
                spec_method == method and _paths_match(spec_path, path)
                for spec_method, spec_path in spec_full_paths
            )
            if not matched:
                undocumented.append(f"  {method} {path}")

        if undocumented:
            print(
                "\n[INFO] Daemon routes NOT in any spec (not a failure):\n"
                + "\n".join(undocumented)
            )
